                         <h2>Instruction </h2><hr>
                            <ol>
                                <li>Pre-heat the oven to 200 degrees F.</li><br>
                                <li>Mix the flour, milk and pepper together in a bowl and let sit for 5 minutes. Adjust the liquid if needed to create a thick but slightly drippy batter.</li><br>
                                <li>Dip the cauliflower florets into the batter and place on a baking sheet lined with parchment paper or a silicone baking mat.</li><br>
                                <li>Roast for 20-25 minutes until starting to get slightly browned.</li><br>
                                <li>Remove from the oven and mix with the buffalo sauce. I just mix it right on the pan.</li><br>
                                <li>Place back in the oven for another 15-20 minutes until browned.</li>
                            </ol><hr>


                        <h2>Ingredients </h2><hr>

                            <ul>
                                <li>1 head of cauliflower, cut into florets (about 4 cups chopped)</li><br>
                                <li>3/4 cup brown rice flour</li><br>
                                <li>3/4 cup unsweetened almond milk (plus more as needed to adjust consistency)</li><br>
                                <li>pinch of black pepper</li><br>
                                <li>1/2 cup Frank’s Buffalo Sauce</li>
                            </ul>

                       

                        
